

USE food_shop_db;

CREATE TABLE accounts
(
	id SERIAL,
	username VARCHAR(50),
	email VARCHAR(50),
	password VARCHAR(50),
	account_type ENUM('client','courier','admin')
);

CREATE TABLE accounts_info
(
	id SERIAL,
	account_id BIGINT UNSIGNED,
	name VARCHAR(50),
	surname VARCHAR(50),
	age INT UNSIGNED,
	balance FLOAT
);

CREATE TABLE products
(
	id SERIAL,
	name VARCHAR(50),
	price FLOAT UNSIGNED,
	category ENUM('Fruits','Vegetables','Snacks','Drinks'),
	country_of_origin VARCHAR(50),
	description VARCHAR(300)
);

CREATE TABLE unfinished_orders
(
	id SERIAL,
	client_id BIGINT UNSIGNED,
	product_id BIGINT UNSIGNED,
	delivery_fee FLOAT UNSIGNED
);

CREATE TABLE orders
(
	id SERIAL,
	client_id BIGINT UNSIGNED,
	courier_id BIGINT UNSIGNED,
	product_id BIGINT UNSIGNED,
	delivery_fee FLOAT UNSIGNED
);

